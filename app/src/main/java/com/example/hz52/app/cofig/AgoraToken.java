package com.example.hz52.app.cofig;

public class AgoraToken {

    private int code;
    private String rtcChannel;
    private String agoraId;
    private String rtcToken;
    private String uid;

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getRtcChannel() {
        return rtcChannel;
    }

    public void setRtcChannel(String rtcChannel) {
        this.rtcChannel = rtcChannel;
    }

    public String getAgoraId() {
        return agoraId;
    }

    public void setAgoraId(String agoraId) {
        this.agoraId = agoraId;
    }

    public String getRtcToken() {
        return rtcToken;
    }

    public void setRtcToken(String rtcToken) {
        this.rtcToken = rtcToken;
    }

    public String getUid() {
        return uid;
    }

    public void setUid(String uid) {
        this.uid = uid;
    }
}
